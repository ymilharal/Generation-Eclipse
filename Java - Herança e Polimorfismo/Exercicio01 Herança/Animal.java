package Exercicio01Herança;

public class Animal {

	private String raça, alimentacao;
	private int idade, estimativaVida;
	public String getRaça() {
		return raça;
	}
	public void setRaça(String raça) {
		this.raça = raça;
	}
	public String getAlimentacao() {
		return alimentacao;
	}
	public void setAlimentacao(String alimentacao) {
		this.alimentacao = alimentacao;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getEstimativaVida() {
		return estimativaVida;
	}
	public void setEstimativaVida(int estimativaVida) {
		this.estimativaVida = estimativaVida;
	}	
}